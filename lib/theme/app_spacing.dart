/// AppSpacing defines the standard spacing constants used throughout the application
/// to maintain consistent layout and spacing between elements.
class AppSpacing {
  /// Basic spacing values
  /// [xxs] - Extra extra small spacing (2.0)
  static const double xxs = 2;

  /// [xs] - Extra small spacing (4.0)
  static const double xs = 4;

  /// [sm] - Small spacing (8.0)
  static const double sm = 8;

  /// [md] - Medium spacing (16.0)
  static const double md = 16;

  /// [lg] - Large spacing (24.0)
  static const double lg = 24;

  /// [xl] - Extra large spacing (32.0)
  static const double xl = 32;

  /// [xxl] - Extra extra large spacing (48.0)
  static const double xxl = 48;

  /// Container padding values
  /// [containerSm] - Small container padding (16.0)
  static const double containerSm = 16;

  /// [containerMd] - Medium container padding (24.0)
  static const double containerMd = 24;

  /// [containerLg] - Large container padding (32.0)
  static const double containerLg = 32;

  /// Section margin values
  /// [sectionSm] - Small section margin (24.0)
  static const double sectionSm = 24;

  /// [sectionMd] - Medium section margin (32.0)
  static const double sectionMd = 32;

  /// [sectionLg] - Large section margin (48.0)
  static const double sectionLg = 48;
}
